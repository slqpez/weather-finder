class Finder{
    constructor(city, country){
        this.city = city
        this.country = country
    }


    getCity(){
        return this.city
    }
    
    setCity(city){
        this.city = city
    }

    getCountry(){
        return this.country
    }

    setCountry(country){
        this.country = country
    }
}

export default Finder

